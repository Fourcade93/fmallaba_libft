#include <string.h>
#include <stdlib.h>

char	*ft_strnew(size_t size)
{
	char	*str;

	str = (char*)malloc(sizeof(char) * (size + 1));
	if (str)
		while (size + 1)
		{
			*str = '\0';
			str++;
			size--;
		}
	else
		return (NULL);
	return (str);
}
