int		ft_atoi(char *str)
{
	int	sign;
	int	res;

	sign = 1;
	res = 0;
	while (*str == ' ' || *str == '\t' || *str == '\n'\
			|| *str == '\v' || *str == '\r' || *str == '\f')
		str++;
	(*str == '+') ? str++ : str;
	if (*str == '-')
	{
		sign = -1;
		str++;
	}
	while (*str > 47 && *str < 58)
		res = res * 10 + (*str++ - '0');
	return (res * sign);
}
